#!/usr/bin/env python3
"""Generate a large pure-POSIX benchmark script that exercises every major
shell subsystem: parsing, expansion, arithmetic, field splitting, globbing,
subshells, command substitution, functions, I/O, redirections, traps, and
control flow.

Usage:
    python3 scripts/gen_bench.py > scripts/bench.sh
    time meiksh scripts/bench.sh
    time bash --posix scripts/bench.sh
    time dash scripts/bench.sh
"""

import textwrap

# Tunable iteration counts -- calibrated so each of the 10 sections takes
# approximately 10 seconds on the release build of meiksh, for a total
# benchmark runtime of ~100 seconds.
ARITH_ITERS = 654_300
EXPAND_ITERS = 308_200
FUNC_ITERS = 32_000
SPLIT_ITERS = 904_700
GLOB_FILES = 500
GLOB_ITERS = 7_000
SUBSHELL_ITERS = 750
IO_WRITE_ITERS = 1_250
IO_HEREDOC_ITERS = 3_125
IO_FD_ITERS = 3_125
TRAP_ITERS = 1_023_300
PARSE_DEPTH = 200
CASE_ARMS = 300
PIPELINE_CHAINS = 150
PARSE_REPEATS = 16_250
COMBINED_ITERS = 785_000


def emit_header():
    return textwrap.dedent("""\
        #!/bin/sh
        # Auto-generated POSIX shell benchmark -- do not edit by hand.
        # Generated by scripts/gen_bench.py

        set -e

        # Run the benchmark in C.UTF-8 so numbers are comparable across hosts
        # and reflect the locale meiksh is optimized for. Fall back to C if
        # the platform has no C.UTF-8 (older glibc, stripped containers).
        for __bench_loc in C.UTF-8 C.utf8 POSIX C; do
            if ( LC_ALL=$__bench_loc /bin/sh -c ':' ) 2>/dev/null; then
                LC_ALL=$__bench_loc
                export LC_ALL
                break
            fi
        done
        unset __bench_loc
        LANG=$LC_ALL
        export LANG

        BENCH_DIR=$(mktemp -d)
        trap 'rm -rf "$BENCH_DIR"' EXIT

        section_time() {
            _label=$1; shift
            _start=$(date +%s)
            "$@"
            _end=$(date +%s)
            printf "  %-40s %ss\\n" "$_label" "$(( _end - _start ))"
        }

        printf "=== meiksh benchmark (LC_ALL=%s) ===\\n" "$LC_ALL"
        BENCH_START=$(date +%s)
    """)


def emit_arithmetic():
    lines = [
        f"bench_arithmetic() {{",
        f"  i=0",
        f"  while [ $i -lt {ARITH_ITERS} ]; do",
    ]
    # Heavy arithmetic — avoid parens inside $(( )) since ) can close the
    # expansion in some shells.  Use temp variables instead.
    lines += [
        "    a=$(( i * 137 + 42 ))",
        "    b=$(( a % 97 ))",
        "    _t1=$(( a << 2 ))",
        "    _t2=$(( b >> 1 ))",
        "    c=$(( _t1 + _t2 ))",
        "    _t3=$(( a & 0xff ))",
        "    d=$(( c ^ _t3 ))",
        "    _t4=$(( b + 1 ))",
        "    e=$(( d | _t4 ))",
        "    f=$(( e > d ? e - d : d - e ))",
        "    g=$(( a != 0 && b != 0 ))",
        "    h=$(( f >= 0 || g <= 1 ))",
        "    acc=$(( a + b + c + d + e + f + g + h ))",
        "    _t5=$(( i + 1 ))",
        "    j=$(( acc / _t5 ))",
        "    k=$(( j * j - j + 17 ))",
        "    _t6=$(( k % 251 ))",
        "    _t7=$(( i & 0x3f ))",
        "    m=$(( _t6 + _t7 ))",
        "    n=$(( ~m + 1 ))",
        "    p=$(( n < 0 ? 0 - n : n ))",
        "    q=$(( p * 3 + i * 7 - 19 ))",
        "    _t8=$(( q >> 3 ))",
        "    r=$(( _t8 & 0xfff ))",
        "    i=$(( i + 1 ))",
        "  done",
        "}",
    ]
    return "\n".join(lines)


def emit_expansion():
    # Two gauntlets run back-to-back: one over an ASCII string, one over a
    # UTF-8 multibyte string. The multibyte string mixes 2- and 3-byte UTF-8
    # sequences with ASCII separators so pattern-removal, ${#var}, and
    # prefix/suffix strips exercise the full character-boundary logic in
    # C.UTF-8.  Iteration count is halved because each pass does the work
    # of the original single gauntlet.
    iters = EXPAND_ITERS // 2
    lines = [
        f"bench_expansion() {{",
        f"  ascii='the-quick-brown-fox-jumps-over-the-lazy-dog/path/to/some/file.tar.gz'",
        # café-ünîcôdé-δοκιμή-テスト-тест/chemin/vers/fichier.tar.gz
        # Mix of Latin-1 (2-byte), Greek (2-byte), CJK (3-byte), Cyrillic
        # (2-byte), all separated by ASCII '-' and '/' so the same pattern
        # operators remain meaningful.
        f"  utf8='café-ünîcôdé-δοκιμή-テスト-тест/chemin/vers/fichier.tar.gz'",
        f"  i=0",
        f"  while [ $i -lt {iters} ]; do",
    ]
    # Parameter expansion gauntlet, applied to both strings in order.
    for var in ("ascii", "utf8"):
        lines += [
            '    a=${' + var + ':-fallback_value}',
            '    b=${' + var + '%.*}',
            '    c=${' + var + '%%.*}',
            '    d=${' + var + '#*/}',
            '    e=${' + var + '##*/}',
            '    f=${#' + var + '}',
            '    g=${' + var + '%.tar.gz}',
            '    h=${' + var + '##*-}',
            '    j=${' + var + '%/*}',
            '    k=${' + var + ':-""}',
            '    m=${' + var + '#*-}',
            '    n=${' + var + '%%/*}',
            '    p=${' + var + '%%-*}',
            '    r=${' + var + '#*-}',
            '    s=${' + var + '%-*}',
            '    : "$a" "$b" "$c" "$d" "$e" "$f" "$g" "$h" "$j" "$k" "$m" "$n" "$p" "$r" "$s"',
        ]
    lines += [
        "    i=$(( i + 1 ))",
        "  done",
        "}",
    ]
    return "\n".join(lines)


def emit_functions():
    # Return results via global variables to avoid fork-heavy $(...) captures.
    lines = [
        "fib_iter() {",
        "  _n=$1; _a=0; _b=1; _j=0",
        "  while [ $_j -lt $_n ]; do",
        "    _t=$(( _a + _b )); _a=$_b; _b=$_t; _j=$(( _j + 1 ))",
        "  done",
        '  FIB_RESULT=$_a',
        "}",
        "",
        "accumulate() {",
        "  _sum=0; _idx=0",
        '  while [ $_idx -lt $1 ]; do',
        "    _sum=$(( _sum + _idx * _idx ))",
        "    _idx=$(( _idx + 1 ))",
        "  done",
        '  ACC_RESULT=$_sum',
        "}",
        "",
        "is_prime() {",
        "  _num=$1",
        "  if [ $_num -lt 2 ]; then PRIME_RESULT=0; return; fi",
        "  _d=2",
        "  _sq=$(( _d * _d ))",
        "  while [ $_sq -le $_num ]; do",
        "    if [ $(( _num % _d )) -eq 0 ]; then PRIME_RESULT=0; return; fi",
        "    _d=$(( _d + 1 ))",
        "    _sq=$(( _d * _d ))",
        "  done",
        "  PRIME_RESULT=1",
        "}",
        "",
        f"bench_functions() {{",
        f"  i=0",
        f"  while [ $i -lt {FUNC_ITERS} ]; do",
        "    fib_iter 30",
        "    accumulate 20",
        "    is_prime $(( i % 997 + 2 ))",
        "    : \"$FIB_RESULT\" \"$ACC_RESULT\" \"$PRIME_RESULT\"",
        "    i=$(( i + 1 ))",
        "  done",
        "}",
    ]
    return "\n".join(lines)


def emit_field_splitting():
    lines = [
        f"bench_splitting() {{",
        f"  data='alpha:bravo:charlie:delta:echo:foxtrot:golf:hotel:india:juliet'",
        f"  i=0",
        f"  while [ $i -lt {SPLIT_ITERS} ]; do",
    ]
    lines += [
        '    old_ifs=$IFS',
        '    IFS=:',
        '    set -- $data',
        '    count=$#',
        '    IFS=$old_ifs',
        '    set -- $data',
        '    count2=$#',
        '    long="aa bb cc dd ee ff gg hh ii jj kk ll mm nn oo pp"',
        '    set -- $long',
        '    count3=$#',
        '    : "$count" "$count2" "$count3"',
        "    i=$(( i + 1 ))",
        "  done",
        "}",
    ]
    return "\n".join(lines)


def emit_globbing():
    lines = [
        f"bench_globbing() {{",
        f'  gdir="$BENCH_DIR/globtest"',
        f'  mkdir -p "$gdir"',
        f"  j=0",
        f"  while [ $j -lt {GLOB_FILES} ]; do",
        f'    : > "$gdir/file_${{j}}.txt"',
        f'    : > "$gdir/data_${{j}}.dat"',
        f'    : > "$gdir/log_${{j}}.log"',
        f"    j=$(( j + 1 ))",
        f"  done",
        f"  i=0",
        f"  while [ $i -lt {GLOB_ITERS} ]; do",
        f'    set -- "$gdir"/*.txt',
        f"    c1=$#",
        f'    set -- "$gdir"/*.dat',
        f"    c2=$#",
        f'    set -- "$gdir"/*.log',
        f"    c3=$#",
        f'    set -- "$gdir"/file_1??.txt',
        f"    c4=$#",
        f'    set -- "$gdir"/*_[0-9].txt',
        f"    c5=$#",
        f'    : "$c1" "$c2" "$c3" "$c4" "$c5"',
        f"    i=$(( i + 1 ))",
        f"  done",
        f'  rm -rf "$gdir"',
        f"}}",
    ]
    return "\n".join(lines)


def emit_subshells():
    lines = [
        f"bench_subshells() {{",
        f"  i=0",
        f"  while [ $i -lt {SUBSHELL_ITERS} ]; do",
        '    a=$(echo "sub_$i" | cat)',
        '    b=$( ( echo inner_a; echo inner_b ) | cat -n )',
        '    c=$(printf "%d %d %d" $i $((i+1)) $((i+2)))',
        "    d=$(echo $i | { read x; echo $(( x * 2 )); })",
        '    : "$a" "$b" "$c" "$d"',
        "    i=$(( i + 1 ))",
        "  done",
        "}",
    ]
    return "\n".join(lines)


def emit_io():
    lines = [
        f"bench_io() {{",
        f'  iofile="$BENCH_DIR/iotest.txt"',
        f'  : > "$iofile"',
        f"  i=0",
        f"  while [ $i -lt {IO_WRITE_ITERS} ]; do",
        f'    echo "line_$i alpha bravo charlie delta echo foxtrot golf hotel" >> "$iofile"',
        f"    i=$(( i + 1 ))",
        f"  done",
        f"  count=0",
        f'  while IFS= read -r line; do',
        f"    count=$(( count + 1 ))",
        f'  done < "$iofile"',
        f"",
        f'  outfile="$BENCH_DIR/ioout.txt"',
        f"  i=0",
        f"  while [ $i -lt {IO_HEREDOC_ITERS} ]; do",
    ]
    # Here-document stress
    lines += [
        '    cat >> "$outfile" <<HEREDOC_END',
        'Line $i: The quick brown fox jumps over the lazy dog.',
        'Expansion: var=${i} arith=$((i * 2 + 1))',
        'More text with "quotes" and special chars: $count',
        'HEREDOC_END',
        "    i=$(( i + 1 ))",
        "  done",
    ]
    # fd juggling
    lines += [
        f'  fdfile="$BENCH_DIR/fdtest.txt"',
        f"  i=0",
        f"  while [ $i -lt {IO_FD_ITERS} ]; do",
        f'    echo "fd_line_$i" 3>"$fdfile" >&3',
        f"    i=$(( i + 1 ))",
        f"  done",
        f"  : \"$count\"",
        f"}}",
    ]
    return "\n".join(lines)


def emit_traps():
    lines = [
        f"bench_traps() {{",
        f"  i=0",
        f"  while [ $i -lt {TRAP_ITERS} ]; do",
        f'    trap ":" USR1',
        f'    trap ":" USR2',
        f'    trap ":" INT',
        f"    trap - USR1",
        f"    trap - USR2",
        f"    trap - INT",
        f"    i=$(( i + 1 ))",
        f"  done",
        f"}}",
    ]
    return "\n".join(lines)


def emit_deep_parse():
    """Generate deeply nested control structures that stress the parser."""
    lines = [f"_bench_parse_once() {{"]
    lines.append("  x=1")

    # Nested if/else chain
    for d in range(PARSE_DEPTH):
        indent = "  " * (d + 1)
        lines.append(f"{indent}if [ $x -eq 1 ]; then")
        lines.append(f"{indent}  x=$(( x + 1 ))")
    # Unwind
    for d in range(PARSE_DEPTH - 1, -1, -1):
        indent = "  " * (d + 1)
        lines.append(f"{indent}fi")

    lines.append("  : \"$x\"")
    lines.append("")

    # Large case statement
    lines.append('  val="item_150"')
    lines.append("  case $val in")
    for arm in range(CASE_ARMS):
        lines.append(f"    item_{arm}) result={arm} ;;")
    lines.append("    *) result=unknown ;;")
    lines.append("  esac")
    lines.append('  : "$result"')
    lines.append("")

    # Long AND-OR chains
    lines.append("  r=0")
    for _ in range(PIPELINE_CHAINS):
        lines.append("  [ $r -ge 0 ] && r=$(( r + 1 )) || r=0")
    lines.append('  : "$r"')

    lines.append("}")
    lines.append("")
    lines.append("bench_parse() {")
    lines.append("  _i=0")
    lines.append(f"  while [ $_i -lt {PARSE_REPEATS} ]; do")
    lines.append("    _bench_parse_once")
    lines.append("    _i=$(( _i + 1 ))")
    lines.append("  done")
    lines.append("}")
    return "\n".join(lines)


def emit_combined_stress():
    """A mixed workload that interleaves multiple subsystems per iteration."""
    lines = [
        "bench_combined() {",
        "  template='hello-world-this-is-a-benchmark-template-string'",
        "  i=0",
        f"  while [ $i -lt {COMBINED_ITERS} ]; do",
        "    a=${template#*-}",
        "    b=${template%-*}",
        "    c=$(( i * 31 + ${#template} ))",
        "    _mod=$(( i % 5 ))",
        "    case $_mod in",
        "      0) d=alpha ;;",
        "      1) d=bravo ;;",
        "      2) d=charlie ;;",
        "      3) d=delta ;;",
        "      *) d=echo ;;",
        "    esac",
        "    if [ $c -gt 1000 ]; then",
        "      e=$(( c - 1000 ))",
        "    else",
        "      e=$c",
        "    fi",
        '    : "$a" "$b" "$c" "$d" "$e"',
        "    i=$(( i + 1 ))",
        "  done",
        "}",
    ]
    return "\n".join(lines)


def emit_footer():
    return textwrap.dedent("""\

        section_time "1. arithmetic"        bench_arithmetic
        section_time "2. parameter expansion" bench_expansion
        section_time "3. function calls"     bench_functions
        section_time "4. field splitting"    bench_splitting
        section_time "5. globbing"           bench_globbing
        section_time "6. subshells"          bench_subshells
        section_time "7. I/O and redirection" bench_io
        section_time "8. trap overhead"      bench_traps
        section_time "9. deep parse"         bench_parse
        section_time "10. combined stress"   bench_combined

        BENCH_END=$(date +%s)
        printf "\\n  TOTAL: %ss\\n" "$(( BENCH_END - BENCH_START ))"
    """)


def main():
    parts = [
        emit_header(),
        emit_arithmetic(),
        "",
        emit_expansion(),
        "",
        emit_functions(),
        "",
        emit_field_splitting(),
        "",
        emit_globbing(),
        "",
        emit_subshells(),
        "",
        emit_io(),
        "",
        emit_traps(),
        "",
        emit_deep_parse(),
        "",
        emit_combined_stress(),
        "",
        emit_footer(),
    ]
    print("\n".join(parts))


if __name__ == "__main__":
    main()
