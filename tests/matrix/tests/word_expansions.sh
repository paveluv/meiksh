# ==============================================================================
# For loop omitting 'in word...'
# ==============================================================================
# REQUIREMENT: SHALL-V3CHAP02-1014:
# Omitting 'in word ...' in for loop is equivalent to in "$@".

_for_out=$($TARGET_SHELL -c 'f() { for i do printf "%s " "$i"; done; }; f x y')
case "$_for_out" in
    *x*y*) pass ;;
