# Pathname Expansion
# ==============================================================================
# REQUIREMENT: SHALL-DESCRIPTION-003:
# Pathname expansion shall not fail due to the size of a file.
# REQUIREMENT: SHALL-2-6-6-187:
# After field splitting, if set -f is not in effect, each field in the
# resulting command line shall be expanded using the algorithm described in 2.14
# Pattern Matching Notation , qualified by the rules in 2.14.3 Patterns Used for
# Filename Expansion .

# We test that `*` expands to files.
touch tmp_path_exp.txt
test_cmd='echo tmp_path_*.txt'
assert_stdout "tmp_path_exp.txt" \
    "$TARGET_SHELL -c '$test_cmd'"

# When `set -f` is in effect, no pathname expansion occurs.
test_cmd='set -f; echo tmp_path_*.txt'
assert_stdout "tmp_path_*.txt" \
    "$TARGET_SHELL -c '$test_cmd'"

# ==============================================================================
# Quote Removal
# ==============================================================================
# REQUIREMENT: SHALL-2-6-7-188:
# The quote character sequence <dollar-sign> single-quote and the
# single-character quote characters (<backslash>, single-quote, and
# double-quote) that were present in the original word shall be removed unless
# they have themselves been quoted.

# Quotes surrounding words are completely removed after all expansions.
test_cmd='echo "quoted" '\''single'\'' \e\s\c\a\p\e\d'
assert_stdout "quoted single escaped" \
    "$TARGET_SHELL -c '$test_cmd'"


# ==============================================================================
# Field splitting from expansions
# ==============================================================================
# REQUIREMENT: SHALL-V3CHAP02-1005-DUP106:
# Shell creates multiple/no fields only from field splitting, pathname
# expansion, or $@/$* expansion.
# REQUIREMENT: SHALL-V3CHAP02-1010:
# Fields from expansion shall be processed in order.

assert_stdout "3" \
    "$TARGET_SHELL -c 'set -- a b c; echo \$#'"

assert_stdout "a b c" \
    "$TARGET_SHELL -c 'x=\"a b c\"; set -- \$x; echo \"\$1 \$2 \$3\"'"

