# REQUIREMENT: SHALL-2-6-3-149:
# Command Substitution: Command substitution
# allows the output of a command to replace the command name...
# REQUIREMENT: SHALL-2-6-3-152:
# Command substitution shall occur when command(s)
# are enclosed as follows: $(commands) or `commands`
# REQUIREMENT: SHALL-2-6-3-149:
# The shell shall expand the command substitution by executing commands in a
# subshell environment (see 2.13 Shell Execution Environment ) and replacing the
# command substitution (the text of the commands string plus the enclosing "$()"
# or backquotes) with the standard output of the command(s); if the output ends
# with one or more bytes that have the encoded value of a <newline> character,
# they shall not be included in the replacement.
# REQUIREMENT: SHALL-2-6-3-151:
# Within the backquoted style of command substitution, if the command
# substitution is not within double-quotes, <backslash> shall retain its literal
# meaning, except when followed by: '$' , '`' , or <backslash>.
# REQUIREMENT: SHALL-2-6-3-152:
# The search for the matching backquote shall be satisfied by the first
# unquoted non-escaped backquote; during this search, if a non-escaped backquote
# is encountered within a shell comment, a here-document, an embedded command
# substitution of the $( commands ) form, or a quoted string, undefined results
# occur.
# REQUIREMENT: SHALL-2-6-3-153:
# With both the backquoted and $( commands ) forms, the commands string shall
# be tokenized (see 2.3 Token Recognition ) and parsed (see 2.10 Shell Grammar
# ).
# REQUIREMENT: SHALL-2-6-3-154:
# Strictly conforming applications shall ensure that the commands string does
# not depend on alias changes taking effect incrementally as would be the case
# if parsed and executed as a program .

# Command substitution is where the shell bends reality, executing a sub-command
# and inserting its stdout directly into the current command line. We test both
# the modern `$()` syntax and the classic backticks.
test_cmd_modern='echo $(echo success)'
assert_stdout "success" \
    "$TARGET_SHELL -c '$test_cmd_modern'"

test_cmd_classic='echo `echo success`'
assert_stdout "success" \
    "$TARGET_SHELL -c '$test_cmd_classic'"

# REQUIREMENT: SHALL-2-6-3-150:
# Any such bytes that occur elsewhere shall be included in the replacement;
# however, they might be treated as field delimiters and eliminated during field
# splitting, depending on the value of IFS and quoting that is in effect.
# REQUIREMENT: SHALL-2-6-3-155:
# The results of command substitution shall not be processed for further tilde
# expansion, parameter expansion, command substitution, or arithmetic expansion.

# Any trailing newlines are stripped. Also, the output doesn't get recursively
# expanded. A `~` inside command substitution output stays a literal `~`.
test_cmd='echo "$(echo "~")"'
assert_stdout "~" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-3-151:
# To specify nesting within the backquoted
# version, the application shall precede the inner backquotes with <backslash>.
# REQUIREMENT: SHALL-2-6-3-157:
# Arithmetic expansion has precedence; that is, the shell shall first determine
# whether it can parse the expansion as an arithmetic expansion and shall only
# parse the expansion as a command substitution if it determines that it cannot
# parse the expansion as an arithmetic expansion.
# REQUIREMENT: SHALL-2-6-3-158:
# If it encounters the end of input without already having determined that it
# cannot parse the expansion as an arithmetic expansion, the shell shall treat
# the expansion as an incomplete arithmetic expansion and report a syntax error.
# REQUIREMENT: SHALL-2-6-3-159:
# A conforming application shall ensure that it separates the "$(" and '(' into
# two tokens (that is, separate them with white space) in a command substitution
# that starts with a subshell.

test_cmd='echo `echo \`echo nested\``'
assert_stdout "nested" \
    "$TARGET_SHELL -c '$test_cmd'"

