assert_stdout "default" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-2-125:
# If parameter is unset or null, quote removal shall be performed on the
# expansion of word and the result (or an empty string if word is omitted) shall
# be assigned to parameter .

# We test `:=` assignment.
