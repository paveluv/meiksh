# ==============================================================================
# The Core Feature: Parameter Expansion
# ==============================================================================
# REQUIREMENT: SHALL-2-6-2-035: Parameter Expansion: The format for parameter
# expansion is as follows: ${expression}

# Braces provide the definitive boundaries for variables, preventing the shell
# from swallowing trailing characters into the variable name.
test_cmd='foo=hello; echo ${foo}'
assert_stdout "hello" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-2-036: Parameter Expansion: The <lbrace> and <rbrace>
# are required except when...

# But the shell is also forgiving; simple identifiers can skip the braces. We
# set `foo` and read it back raw.
test_cmd='foo=world; echo $foo'
assert_stdout "world" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-2-124: If parameter is unset or null, the expansion of
# word (or an empty string if word is omitted) shall be substituted. Otherwise,
# the value of parameter shall be substituted.
# [Use Default Values: ${parameter:-word}]

# We test `:-` fallback.
test_cmd='unset foo; echo "${foo:-default}"'
assert_stdout "default" \
    "$TARGET_SHELL -c '$test_cmd'"

test_cmd='foo=set; echo "${foo:-default}"'
assert_stdout "set" \
    "$TARGET_SHELL -c '$test_cmd'"

test_cmd='foo=""; echo "${foo:-default}"'
assert_stdout "default" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-2-125: If parameter is unset or null, quote removal
# shall be performed on the expansion of word and the result shall be assigned
# to parameter. In all cases, the final value of parameter shall be substituted.
# [Assign Default Values: ${parameter:=word}]

# We test `:=` assignment.
test_cmd='unset foo; echo "${foo:=assigned}"; echo "$foo"'
assert_stdout "assigned
assigned" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-2-126: If parameter is unset or null, the expansion of
# word (or a message indicating it is unset if word is omitted) shall be
# written to standard error and the shell exits with a non-zero exit status.
# [Indicate Error if Unset or Null: ${parameter:?word}]

# We test `:?` error generation.
test_cmd='unset foo; echo "${foo:?error message}"'
assert_stderr_contains "error message" \
    "$TARGET_SHELL -c '$test_cmd'"

assert_exit_code_non_zero \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-2-127: If parameter is unset or null, null shall be
# substituted. Otherwise, the expansion of word (or an empty string if word is
# omitted) shall be substituted. [Use Alternative Value: ${parameter:+word}]

# We test `:+` alternative value substitution.
test_cmd='unset foo; echo "${foo:+alt}"'
assert_stdout "" \
    "$TARGET_SHELL -c '$test_cmd'"

test_cmd='foo=set; echo "${foo:+alt}"'
assert_stdout "alt" \
    "$TARGET_SHELL -c '$test_cmd'"

