# Math Class: Arithmetic Expansion
# ==============================================================================
# REQUIREMENT: SHALL-2-6-4-165:
# Arithmetic Expansion: The format for arithmetic
# expansion shall be as follows: $((expression))
# REQUIREMENT: SHALL-V3CHAP02-1008:
# The format for arithmetic expansion shall be as follows: $(( expression ))
# The expression shall be treated as if it were in double-quotes, except that a
# double-quote inside the expression is not treated specially.
# REQUIREMENT: SHALL-2-6-4-161:
# The expression shall be treated as if it were in double-quotes, except that a
# double-quote inside the expression is not treated specially.
# REQUIREMENT: SHALL-2-6-4-163:
# Next, the shell shall treat this as an arithmetic expression and substitute
# the value of the expression.
# REQUIREMENT: SHALL-2-6-4-163:
# The arithmetic expression shall be processed
# according to the rules given in 1.1.2.1 Arithmetic Precision...

# POSIX shells double as calculators. By wrapping an expression in double
# parentheses, the shell evaluates the arithmetic and expands to the integer
# result.
test_cmd='echo $((2 + 2))'
assert_stdout "4" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-4-162:
# The shell shall expand all tokens in the expression for parameter expansion,
# command substitution, and quote removal.
# REQUIREMENT: SHALL-2-6-4-166:
# If the shell variable x contains a value that forms a valid integer constant,
# optionally including a leading <plus-sign> or <hyphen-minus>, then the
# arithmetic expansions "$((x))" and "$(($x))" shall return the same value.

test_cmd='x=5; echo $((x * $(echo 2)))'
assert_stdout "10" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-4-165:
# All changes to variables in an arithmetic expression shall be in effect after
# the arithmetic expansion, as in the parameter expansion "${x=value}" .

test_cmd='x=1; echo $((x = x + 4)); echo "$x"'
assert_stdout "5
5" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-4-167:
