# ==============================================================================
# The Grand Order of Operations
# ==============================================================================
# REQUIREMENT: SHALL-2-6-110: When the expansions in this section are performed
# other than in the context of preparing a command f...
# REQUIREMENT: SHALL-2-6-111: When expanding words for a command about to be
# executed, and the word will be the command name or an...
# REQUIREMENT: SHALL-2-6-112: If a '$' that is neither within single-quotes
# nor escaped by a <backslash> is immediately followed b...
# REQUIREMENT: SHALL-2-6-101: The expansions that are performed for a given word
# shall be performed in the following order:
# REQUIREMENT: SHALL-2-6-102: Tilde expansion, parameter expansion, command
# substitution, and arithmetic expansion shall be performed...
# REQUIREMENT: SHALL-2-6-103: Field splitting shall be performed on the portions
# of the fields generated by step 1...
# REQUIREMENT: SHALL-2-6-104: Pathname expansion shall be performed, unless set
# -f is in effect.
# REQUIREMENT: SHALL-2-6-105: Quote removal, if performed, shall always be
# performed last.

# We test the entire sequence by constructing a word that relies on the correct
# order:
# 1. Parameter expansion happens first ($foo -> "~ *").
# 2. Field splitting occurs next (splitting "~" and "*").
# 3. Pathname expansion happens on the "*" (turning it into file names).
# 4. Quote removal removes the quotes on the original word (none in this case,
#    but we test quote removal separately).
# Note: Tilde expansion is NOT performed after parameter expansion.
touch file1 file2
test_cmd='foo="~ *"; printf "%s\n" $foo'
assert_stdout '~
file1
file2' \
    "$TARGET_SHELL -c '$test_cmd'"
rm file1 file2

# ==============================================================================
# The Magic of the Tilde
# ==============================================================================
# REQUIREMENT: SHALL-2-6-1-030: Tilde Expansion: A <tilde> ('~') character shall
# be subject to tilde expansion if it is the first character...
# REQUIREMENT: SHALL-2-6-1-113: If the tilde-prefix consists of only the <tilde>
# character, it shall be replaced by the value of the HOME variable.

# When a path starts with a lonely `~`, the shell is supposed to gracefully
# unfold it into the user's home directory. Let's make sure it knows the
# way home.
test_cmd='echo ~'
assert_stdout "$HOME" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-1-117: The pathname that replaces the tilde-prefix
# shall be treated as if quoted to prevent it being altered by later expansions.

# If `$HOME` contains a space or a wildcard, it should not be subjected to field
# splitting or pathname expansion. We test this by forcing `$HOME` to a string
# containing spaces and asterisks.
test_cmd='HOME="home with * spaces"; printf "%s\n" ~'
assert_stdout "home with * spaces" \
    "$TARGET_SHELL -c '$test_cmd'"


