    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-2-124:
# If parameter is unset or null, the expansion of word (or an empty string if
# word is omitted) shall be substituted; otherwise, the value of parameter shall
# be substituted.

# We test `:-` fallback.
test_cmd='unset foo; echo "${foo:-default}"'
assert_stdout "default" \
    "$TARGET_SHELL -c '$test_cmd'"

test_cmd='foo=set; echo "${foo:-default}"'
assert_stdout "set" \
    "$TARGET_SHELL -c '$test_cmd'"

