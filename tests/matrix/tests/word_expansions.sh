test_cmd='echo $((1 / 0))'
assert_exit_code_non_zero \
    "$TARGET_SHELL -c '$test_cmd'"


# ==============================================================================
# Field Splitting
# ==============================================================================
# REQUIREMENT: SHALL-2-6-5-168:
# After parameter expansion ( 2.6.2 Parameter Expansion ), command substitution
# ( 2.6.3 Command Substitution ), and arithmetic expansion ( 2.6.4 Arithmetic
# Expansion ), if the shell variable IFS (see 2.5.3 Shell Variables ) is set and
# its value is not empty, or if IFS is unset, the shell shall scan each field
# containing results of expansions and substitutions that did not occur in
# double-quotes for field splitting; zero, one or multiple fields can result.
# REQUIREMENT: SHALL-2-6-5-169:
# For the remainder of this section, any reference to the results of an
# expansion, or results of expansions, shall be interpreted to mean the results
# from one or more unquoted variable or arithmetic expansions, or unquoted
# command substitutions.
# REQUIREMENT: SHALL-2-6-5-170:
# If the IFS variable is set and has an empty string as its value, no field
# splitting shall occur.
# REQUIREMENT: SHALL-2-6-5-172:
# After the removal of any such fields from the input, the possibly modified
# input field list shall become the output.
# REQUIREMENT: SHALL-2-6-5-173:
# Each input field shall be considered in sequence, first to last, with the
# results of the algorithm described in this section causing output fields to be
# generated, which shall remain in the same order as the input fields from which
# they originated.
# REQUIREMENT: SHALL-2-6-5-176:
# The shell shall use the byte sequences that form the characters in the value
# of the IFS variable as delimiters.
# REQUIREMENT: SHALL-2-6-5-183:
# No such byte sequence shall contain data such that some bytes in the sequence
# resulted from an expansion, and others did not, nor which contains bytes
# resulting from the results of more than one expansion.
# REQUIREMENT: SHALL-2-6-5-184:
# At this point, if the candidate is not empty, or if a sequence of bytes
# representing an IFS character that is not IFS white space was seen at step 4,
# then a field is said to have been delimited, and the candidate shall become an
# output field.
# REQUIREMENT: SHALL-2-6-5-185:
# Once the input is empty, the candidate shall become an output field if and
# only if it is not empty.
# REQUIREMENT: SHALL-2-6-5-186:
# The ordered list of output fields so produced, which might be empty, shall
# replace the list of input fields.

# We test that spaces inside a variable are not split when IFS is empty.
test_cmd='IFS=""; foo="a b c"; for i in $foo; do echo "arg: $i"; done'
assert_stdout "arg: a b c" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-5-175:
# If the IFS variable is unset, then for the purposes of this section, but
# without altering the value of the variable, its value shall be considered to
# contain the three single-byte characters <space>, <tab>, and <newline> from
# the portable character set, all of which are IFS white-space characters.
# REQUIREMENT: SHALL-2-6-5-178:
# The shell shall use these delimiters as field terminators to split the
# results of expansions, along with other adjacent bytes, into separate fields,
# as described below.

# By default, unset IFS splits on space, tab, and newline.
test_cmd='unset IFS; foo="a b	c
d"; for i in $foo; do echo "arg: $i"; done'
assert_stdout "arg: a
arg: b
arg: c
arg: d" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-5-177:
# Each of the characters <space>, <tab>, and <newline> which appears in the
# value of IFS shall be a single-byte delimiter.

test_cmd='IFS=" "; foo="a   b"; for i in $foo; do echo "arg: $i"; done'
assert_stdout "arg: a
arg: b" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-5-174:
# Fields which contain no results from expansions shall not be affected by
# field splitting, and shall remain unaltered, simply moving from the list of
# input fields to be next in the list of output fields.

# Unexpanded literals are never split, even if they contain IFS characters.
test_cmd='IFS=","; for i in a,b,c; do echo "arg: $i"; done'
assert_stdout "arg: a,b,c" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-5-171:
# However, if an input field which contained the results of an expansion is
# entirely empty, it shall be removed.

test_cmd='IFS=" "; foo=""; for i in a $foo b; do echo "arg: $i"; done'
assert_stdout "arg: a
arg: b" \
    "$TARGET_SHELL -c '$test_cmd'"

# REQUIREMENT: SHALL-2-6-5-179:
# If the results of the algorithm are that no fields are delimited; that is, if
# the input field is wholly empty or consists entirely of IFS white space, the
# result shall be zero fields (rather than an empty field).
# REQUIREMENT: SHALL-2-6-5-180:
# For the purposes of this section, when a field is said to be delimited, then
# the candidate field, as generated below shall become an output field.
# REQUIREMENT: SHALL-2-6-5-181:
# When the algorithm transforms a candidate into an output field it shall be
# appended to the current list of output fields.
# REQUIREMENT: SHALL-2-6-5-168:
# Each field containing the results from an
# expansion shall be processed in order...

test_cmd='IFS="x"; foo="abc"; for i in $foo; do echo "arg: $i"; done'
assert_stdout "arg: abc" \
    "$TARGET_SHELL -c '$test_cmd'"


